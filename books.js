function applyBookEdits(book, edits) {
  const bookEdits = edits?.[book.id];
  if (!bookEdits) {
    return book;
  }

  return {
    ...book,
    ...bookEdits
  };
}

window.loadBooks = function loadBooks() {
  const customBooks = window.getCustomBooks ? window.getCustomBooks() : JSON.parse(localStorage.getItem('bookShelfCustomBooks') ?? '[]');
  const editedBooks = window.getBookEdits ? window.getBookEdits() : JSON.parse(localStorage.getItem('bookShelfEditedBooks') ?? '{}');
  const defaultBooks = window.DEFAULT_BOOKS.map((book) => applyBookEdits(book, editedBooks));
  const mergedCustomBooks = customBooks.map((book) => applyBookEdits(book, editedBooks));
  return [...defaultBooks, ...mergedCustomBooks];
};

window.refreshBooks = function refreshBooks() {
  window.BOOKS = window.loadBooks();
  return window.BOOKS;
};

window.refreshBooks();